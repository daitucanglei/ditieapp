package com.example.lsm;

public class xiewen {
    private  String xin;
    private  String lanmu;

    public xiewen(String xin, String lanmu) {
        this.xin = xin;
        this.lanmu = lanmu;
    }

    public String getXin() {
        return xin;
    }

    public void setXin(String xin) {
        this.xin = xin;
    }

    public String getLanmu() {
        return lanmu;
    }

    public void setLanmu(String lanmu) {
        this.lanmu = lanmu;
    }
}
